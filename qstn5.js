let value = 144;

if (value % 2 === 0) {
  if (value > 100) {
    console.log("Even and greater than 100");
  } else {
    console.log("Even and less than or equal to 100");
  }
} else {
  if (value > 100) {
    console.log("Odd and greater than 100");
  } else {
    console.log("Odd and less than or equal to 100");
  }
}
